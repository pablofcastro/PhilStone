/**
 * An enumeration class for the possible operators in the AST
 * @author Pablo
 *
 */
public enum Operator {
	SUM, 
	MULT, 
	DIV, 
	NEG, 
	AND, 
	OR, 
	NOT, 
	IMP, 
	AU, 
	EU, 
	AX, 
	EX, 
	VAR,
	EQ,
	EG,
	AG,
	EF,
	AF,
	EW,
	AW,
	BCONS,
	ICONS,
	MINUS,
	AV,
	OWN,
	INC, // for ENUM, returns the next element
	DEC  // for ENUM returns the previous element
}

